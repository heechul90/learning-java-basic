package dataType;

public class Number {
	public static void main(String[] args) {
		
		// 사칙연산
        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(7%3);  // 나머지 출력
        System.out.println(3%7);  // 나머지 출력
        
        // 증감연산 (++,--)
        int i = 0;
		int j = 10;
		i++;
		j--;
		System.out.println(i);
		System.out.println(j);
    }
}
